"""
    ClimStats

An API for retrieving and analysing climate data for a single location, from
ERA5 reanalysis (the past) and CMIP6 climate projections (the future).

Quick start
-----------
```julia
using ClimStats
using CairoMakie            # a Makie backend: CairoMakie for files, GLMakie for windows

# One-liner: location string in, figure out.
fig = climate_timeseries("Berlin, Germany"; threshold = 30)
save("berlin_hot_days.png", fig)   # `save` re-exported from Makie

# Or step by step, to compute many things from the same download.
data   = era5_daily("Berlin, Germany")     # daily tmax/tmin/tmean/precip
hot    = days_above(data, 30)              # days/yr with Tmax > 30 °C
frost  = frost_days(data)                  # days/yr with Tmin < 0 °C
warming = annual_mean(data; var = :tmean)  # yearly mean temperature
plot_index(hot)
```

Historical data come from a keyless provider via `history_daily`, which defaults
to **NASA POWER** (`power_daily`; no request quota, 1981→present) and can switch
to the Open-Meteo **ERA5** archive (`era5_daily`; 1940→present) with
`source = :era5`. The CMIP6 ensemble (`projection_daily`) is fetched from the
key-less Open-Meteo climate API; SSP-scenario projections use the NASA
NEX-GDDP-CMIP6 backend (`nexgddp_daily`, `ssp_ensemble`, `climate_ssp`), enabled
by `using NCDatasets`. All are cached on disk on first fetch, and the example
locations ship with a bundled offline fixture cache. Plots are built with Makie
(core); load a backend (`CairoMakie` to save files, `GLMakie`/`WGLMakie` for
interactive use) to render or `save` the returned `Figure`. See also the index
helpers (`days_above`, `frost_days`, `annual_mean`, …), `bias_correct` and
`plot_index`.
"""
