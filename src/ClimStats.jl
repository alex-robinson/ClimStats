# Directory of the in-repo bundled fixture cache: committed Arrow series for the
# example locations, generated by `scripts/generate_fixtures.jl`. Laid out as a
# DataManifest datacache pool (`<cachetype>/<hash>/…`).
const FIXTURE_DIR = normpath(joinpath(@__DIR__, "..", "data", "fixtures"))

# Register the bundled fixtures as a DataManifest *read pool*, so the example
# locations resolve straight from disk — offline and quota-free — with no setup.
# Prepended idempotently, preserving any user-set `DATAMANIFEST_DATACACHE_POOLS`.
function _register_fixture_pool()
    isdir(FIXTURE_DIR) || return nothing
    key = "DATAMANIFEST_DATACACHE_POOLS"
    sep = Sys.iswindows() ? ';' : ':'
    existing = get(ENV, key, "")
    parts = isempty(existing) ? String[] : split(existing, sep)
    FIXTURE_DIR in parts && return nothing
    ENV[key] = isempty(existing) ? FIXTURE_DIR : string(FIXTURE_DIR, sep, existing)
    return nothing
end

# Offline mode can be switched on at load time via the environment, so a process
# started with `CLIMSTATS_OFFLINE=1` never touches the network.
function __init__()
    _OFFLINE[] = lowercase(strip(get(ENV, "CLIMSTATS_OFFLINE", ""))) in
                 ("1", "true", "yes", "on")
    _register_fixture_pool()
    return nothing
end

